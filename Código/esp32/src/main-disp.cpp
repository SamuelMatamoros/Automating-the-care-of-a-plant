// Array of all bitmaps. (Total bytes used to store images in PROGMEM = 16080)
const int iconsArray_LEN = 5;
const unsigned char* iconsArray[5] = {
	icons_Clock,
	icons_Face,
	icons_Luz,
	icons_Menu,
	icons_Rigeo
};

//PWM variables
int pwmPin = 2;
int pwmChannel = 0;
int pwmRes = 8;
int pwdFrequency = 1000;
int dt = 5;

//Screen selection variables
//Selected
int current_icon = 0;
//Previous
int previous_icon = iconsArray_LEN - 1;
//Next
int next_icon = current_icon + 1;

//Rotary encoder inclusion variables
#define DTPin 21
#define CLKPin 19
#define button 22

int OldStateCLK;
int NewStateCLK;
int counter = 0;


//////////////////////////////
//Startup animation function//
//////////////////////////////

void startupAnimation(){

	//TFT initiation animation// (turn into a function and call it out in the setup at startup)
	for (int k = 0; k < 5; k++){

	for (int i = 0; i < 255; i++){
		ledcWrite(pwmChannel, i);
		delay(dt);
	}
	for (int j = 255; j >= 0; j--){
		ledcWrite(pwmChannel, j);
		delay(dt);
	}
	delay(100);
	}

	delay(1000);
	tft.fillScreen(TFT_BLACK);

	ledcWrite(pwmChannel, 255);
}

void drawSelectionMenu() {

	//////////////////////////////
	//Drawing the selection menu//
	//////////////////////////////

	//Previous icon
	tft.drawBitmap(0,0,iconsArray[previous_icon],320,80,TFT_WHITE, TFT_BLACK);

	//Current icon
	tft.drawBitmap(0,80,iconsArray[current_icon],320,80,TFT_WHITE, TFT_BLACK);

	//Next icon
	tft.drawBitmap(0,160,iconsArray[next_icon],320,80,TFT_WHITE, TFT_BLACK);

	//Selection square
	tft.drawBitmap(0,80,icons_Select,320,80,TFT_WHITE);

}

//////////////////////
//		Setup		//
//////////////////////

void setup() 
{
	Serial.begin(9600);

	//knob setup//
	pinMode(DTPin, INPUT);
	pinMode(CLKPin, INPUT);
	pinMode(button, INPUT);

	OldStateCLK = digitalRead(CLKPin);

	//PWM setup//
	ledcSetup(pwmChannel, pwdFrequency, pwmRes);
	ledcAttachPin(pwmPin, pwmChannel);

	//TFT setup//
	tft.init();
	tft.setRotation(3);
	tft.fillScreen(TFT_BLACK);
	tft.setTextColor(TFT_WHITE);
	tft.setTextSize(6);
	tft.setCursor(80, 100);
	tft.println("MA-z");

	startupAnimation();
	drawSelectionMenu();
}

//////////////////////
//		Loop		//
//////////////////////

void loop() 
{

	//////////////////////////////
	//Geting rotary encoder info//
	//////////////////////////////

	NewStateCLK = digitalRead(CLKPin);
    if (NewStateCLK != OldStateCLK) {
		if (digitalRead(DTPin) == NewStateCLK) {counter++;}	
		else {counter--;}	
		if (counter < 0) {counter = iconsArray_LEN - 1;}
		if (counter > iconsArray_LEN - 1) {counter = 0;}
		
		current_icon = counter;

		previous_icon = current_icon - 1;
		if (previous_icon < 0) {previous_icon = iconsArray_LEN - 1;}
		next_icon = current_icon + 1;
		if (next_icon > iconsArray_LEN - 1) {next_icon = 0;}

		drawSelectionMenu();

	}


    OldStateCLK = NewStateCLK;
}